//Ejemplo de un incremento
#include <iostream>
#include <string>
using namespace std;

int main()
{
int numero1,resultado;
cout<<"Escribe el valor de numero1: ";
cin>>numero1;
resultado=++numero1;
cout<<"numero1="<<numero1<<endl; //operacion, asigancion
cout<<"resultado="<<resultado<<endl;

return 0;
}

